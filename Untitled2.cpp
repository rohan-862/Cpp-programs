#include<iostream>
using namespace std;

int main()
{
	int num1, num2;
	cout<<"Enter the value of num1:\n"; /* << is called Insertion opperator */
	cin>>num1; /* '>>' is called Extraction opperator */
	
	cout<<"Enter the value of num2:\n"; /* << is called Insertion opperator */
	cin>>num2; /* '>>' is called Extraction opperator */
	
	cout<<"The sub is "<< num1+ num2;
	
	return 0;
}
